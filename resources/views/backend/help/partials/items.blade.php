<h2 class="help-topic-header" id="posts">Posts</h2>
<p>
    Create a new post by visiting the <span class="zmdi zmdi-collection-bookmark"></span> Posts
    menu item in the sidebar, and then clicking the <span class="zmdi zmdi-plus-circle text-primary"></span> icon on the <a href="{{ url('admin/posts') }}">Posts</a> page. If you get interrupted and want to save your work for later, just toggle the
    <code>Draft?</code> button and click
    <span class="label label-primary"><span class="zmdi zmdi-floppy"></span> PUBLISH</span> to make
    sure that the post is not visible on your blog yet.
</p>
<p>
    The fastest and easiest way to write in Canvas is to utilize Markdown. The split-screen editor allows you full-screen flexibility and live formatting to make your writing experience smooth and effortless. For a little bit of a beginner's primer or to just brush up on your skills, take a look at the <a href="{{ url('/blog/hello-world') }}">Hello World</a> post.
</p>
<p>
    From the <a href="{{ url('admin/post') }}">Posts</a> overview page, you can click the <span class="zmdi zmdi-edit text-primary"></span> icon next to each
    post to update its contents or the <span class="zmdi zmdi-search text-primary"></span> icon to see what it looks like to
    your readers.
</p>
<p>
    To delete a post, go to the <a href="{{ url('admin/posts') }}">Posts</a> page and click the <span class="zmdi zmdi-edit text-primary"></span> action on a specific post.
    On the following page, click <span class="label label-danger"><span class="zmdi zmdi-delete"></span> DELETE</span> to completely remove the post from the blog.
</p>
<br>

<h2 class="help-topic-header" id="tags">Tags</h2>
<p>
    Tags provide grouping and organization to your blog posts. You can create a new tag by visiting the <span class="zmdi zmdi-labels"></span> Tags
    menu item in the sidebar, and then clicking the <span class="zmdi zmdi-plus-circle text-primary"></span> icon on the <a href="{{ url('admin/tags') }}">Tags</a> page.
    Once you specify the name, title and subtitle, click <span class="label label-primary"><span class="zmdi zmdi-floppy"></span> SAVE</span> to
    store your new tag in the database.
</p>
<p>
    Assigning a tag to a specific blog post is easy. Just go to the <a href="{{ url('admin/posts') }}">Posts</a> page and click the <span class="zmdi zmdi-edit text-primary"></span> action
    of whichever post you want to assign the tag to. Within the form, click <code>Tags</code> and select the name of the tag you just created. Once you click
    <span class="label label-primary"><span class="zmdi zmdi-floppy"></span> SAVE</span> at the bottom of the page, the blog post will be
    grouped under that specific tag.
</p>
<p>
    To delete a tag, go to the <a href="{{ url('admin/tags') }}">Tags</a> page and click the <span class="zmdi zmdi-edit text-primary"></span> action on a specific tag.
    On the following page, click <span class="label label-danger"><span class="zmdi zmdi-delete"></span> DELETE</span> to completely remove the tag from the blog.
</p>
